const getPublicationRequest = (publicationId: string) => {
  return apolloClient.query({
    query: gql(GET_PUBLICATION),
    variables: {
      request: {
        publicationId,
      },
    },
    fetchPolicy: "no-cache",
  });
};

export const getPublication = async (publicationId: string) => {
  const result = await getPublicationRequest(publicationId);
  prettyJSON("publication: result", result.data);

  return result.data.publication;
};

